package streamexam;

public class Student2 {
	private String name;
	private String gender;
	private int score;

	public Student2(String name, String gender, int score) {
		this.name = name;
		this.gender = gender;
		this.score = score;
	}

	public String getName() {
		return name;
	}

	public String getGender() {
		return gender;
	}

	public int getScore() {
		return score;
	}
}